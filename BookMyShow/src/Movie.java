

public class Movie {
	int movieId;
	String movieName;
	int movieDurationInMinuites;
	
	//getters and setters
	
	public int getMovieId() {
		return movieId;
	}
	public void setMovieId(int movieId) {
		this.movieId = movieId;
	}
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public int getMovieDurationInMinuites() {
		return movieDurationInMinuites;
	}
	public void setMovieDurationInMinuites(int movieDurationInMinuites) {
		this.movieDurationInMinuites = movieDurationInMinuites;
	}
	
	
	
	
	
	
}
